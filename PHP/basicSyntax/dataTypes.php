<?php
    // 文字列型　string
    var_dump("Hello"); 

    // 整数型　int
    var_dump(10);
    var_dump(-3);

    // 論理型　bool
    var_dump(3 == 3);

    // 浮動小数点数型　float
    var_dump(3.14);

    // 配列型　array
    var_dump($a = ["apple", "banana"]);

    // NULL型　
    var_dump($null);

    